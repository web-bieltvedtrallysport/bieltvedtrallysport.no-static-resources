function Picture(theFile, theText) {
	this.file = theFile;
	this.thumbnailFile = '-' + theFile;
	this.text = theText;
}


function PictureCollection(theTitle, theImagesFolder, thePictures) {
	pictureCollections.push(this);
	this.title = theTitle;
	this.imagesFolder = theImagesFolder;
	this.pictures = thePictures;
}


function writeThumbnailTable(collectionIndex) {
	var html = '<table id="thumbnailtable" cellpadding="5" cellspacing="3" width="100%" border="0">\n';
	var newRow = true;
	var pictureCollection = pictureCollections[collectionIndex];
	var pictures = pictureCollection.pictures;
	for (i = 0; i < pictures.length; i++) {
		if (newRow) {
			html += '<tr>\n';
		}
		html += '<td width="50%">\n';
		html += '	<a href="javascript:void(openWindow(\'popup-fotoalbum.html?' + collectionIndex + '-' + i + '\', 720, 580, 300, 300));"><img src="' + pictureCollection.imagesFolder + pictures[i].thumbnailFile + '" alt="' + pictures[i].text + '"></a><br>\n';
		html += '	' + pictures[i].text + '\n';
		html += '</td>\n';		
		if (!newRow || i == pictureCollection.pictures.length - 1) {
			html += '</tr>\n';
		}
		
		newRow = !newRow;
	}
	html += '</table>\n';
	
	var thumbnailsElement = document.getElementById('thumbnails');
	thumbnailsElement.innerHTML = html;
	thumbnailsElement.style.height = document.getElementById('thumbnailtable').offsetHeight + 'px';
	//resizeParentContainer();
}